def main():
    loop = asyncio.new_event_loop()
    loop.run_until_complete(async_main())
    loop.run_forever()


async def async_main():
    client = await datacenter.create_client()

    # 启动断线侦听协程，用于断线触发警报
    asyncio.create_task(wait_close(client.close_future))

    now_symbol = '\\'
    while True:
        await asyncio.sleep(1)

        code = await client.get_precise(['alarm', 'common', 'code'])
        reason = await client.get_precise(['alarm', 'common', 'reason'])

        # 是0则需要触发警报
        if code == 0:
            print('code为0，触发警报', reason)
            alarm()

        # 服务端超过20s没有更新，也要触发警报
        if code is None or time.time() - code > 20000:
            print('code为None或者超过20s未更新，触发警报', code)
            alarm()

        # 刷新旋转符号
        if now_symbol == '\\':
            now_symbol = '/'
        elif now_symbol == '/':
            now_symbol = '-'
        elif now_symbol == '-':
            now_symbol = '\\'
        print('\r监控系统正常运作中...' + now_symbol, end='')


async def wait_close(future):
    """
    等待websocket连接close并触发警报
    """
    await future
    print('websocket连接断开，触发警报')
    alarm()


if __name__ == '__main__':
    main()
